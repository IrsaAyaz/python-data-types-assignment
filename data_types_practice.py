#Integer Examples
grade = 7
score = 99
print("Grade:", grade)
print("Score:", score)

#String Examples
name = "Irsa Ayaz"
message = "Learning Python is really fun!"
print("Name:", name)
print("Message:", message)

#Float Examples
milkQuantity = 20.55 #in Litters
price = 10.99 #in Rupees
print("MilkQuantity:", milkQuantity)
print("Price:", price)

#Boolean Examples
is_hungry = True
not_hungry = False
print("Is Hungry:", is_hungry)
print("Not Hungry:", not_hungry)